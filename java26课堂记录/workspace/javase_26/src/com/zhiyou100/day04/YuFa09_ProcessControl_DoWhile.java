package com.zhiyou100.day04;

public class YuFa09_ProcessControl_DoWhile {

	public static void main(String[] args) {
	      int a;
	      a=10;
	      while(a<=5) {
	    	  System.out.println("while::::a="+a);
	    	  a++;
	      }
	      
	      a=10;
	      do{
	    	  System.out.println("do while:::::::a="+a);
	    	  a++;
	      }while(a<=5);

	}

}
